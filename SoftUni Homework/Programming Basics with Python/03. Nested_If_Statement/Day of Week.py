number = int(input())

if number == int('1'):
    print("Monday")
elif number == int("2"):
    print("Tuesday")
elif number == int("3"):
    print("Wednesday")
elif number == int("4"):
    print("Thursday")
elif number == int("5"):
    print("Friday")
elif number == int("6"):
    print("Saturday")
elif number == int("7"):
    print("Sunday")
else:
    print("Error")
